#Definiendo una variable
nombre = "Enrique "


#conacatenar con +
bienvenido = f"Hola " + " ¿Como estas?"

# conacatenar con f strings 
bienvenido = f"Hola {nombre} ¿Como estas?"



#operadores de pertenencia (in / no in)
print("Enrique" in bienvenido) #true

print("Enrique" not in bienvenido) #false